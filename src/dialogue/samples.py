def build_sample_dialogue() -> Dialogue:
    """Готовый демонстрационный диалог с фиксированными id."""
    created = datetime(2026, 1, 1, tzinfo=timezone.utc)
    turns = [
        DialogueTurn(
            id=Id.from_str(turn_id),
            role=role,
            content=content,
            timestamp=created,
        )
        for turn_id, role, content in _SAMPLE_TURNS
    ]
    return Dialogue(id=SAMPLE_DIALOGUE_ID, turns=turns, created_at=created)
