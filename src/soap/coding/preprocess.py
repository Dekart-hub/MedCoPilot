"""Нормализация текста для лексического матчинга диагнозов (Tier 1).

Одна и та же функция применяется к формулировкам корпуса (Том 3, офлайн) и к
тексту ассессмента (онлайн): раскрытие частых аббревиатур, токенизация,
выкид стоп-слов и грубый стемминг. Цель — мешок токенов, инвариантный к
порядку слов (Том 3 пишет номенклатурно: «Флегмона головы») и к словоформам.

Это сознательно простой baseline без внешних зависимостей. Первый апгрейд на
v1 — заменить ``_stem`` на полноценную лемматизацию (pymorphy3).
"""

from __future__ import annotations

import re

_TOKEN_RE = re.compile(r"[а-яёa-z0-9]+")

# Раскрытие частых клинических аббревиатур. Ключи — «сырые» токены до стемминга.
# Список намеренно короткий и бесспорный; расширяется по мере анализа данных.
ABBREVIATIONS: dict[str, str] = {
    "сд": "сахарный диабет",
    "сах": "сахарный",
    "хобл": "хроническая обструктивная болезнь легких",
    "ибс": "ишемическая болезнь сердца",
    "хсн": "хроническая сердечная недостаточность",
    "аг": "артериальная гипертензия",
    "гб": "гипертоническая болезнь",
    "онмк": "острое нарушение мозгового кровообращения",
    "орви": "острая респираторная вирусная инфекция",
    "оки": "острая кишечная инфекция",
}

# Стоп-слова: только служебные части речи, чтобы не выкидывать значимое.
STOPWORDS: frozenset[str] = frozenset(
    {
        "и", "или", "с", "со", "без", "на", "в", "во", "при", "от", "до",
        "по", "для", "над", "под", "из", "за", "к", "о", "об",
    }
)

# Инфлекционные окончания, от длинных к коротким. Снимаем максимум одно.
_SUFFIXES: tuple[str, ...] = (
    "иями", "ями", "ами", "ого", "его", "ыми", "ими", "ому", "ему",
    "ая", "яя", "ое", "ее", "ый", "ий", "ой", "ом", "ем", "ах", "ях",
    "ов", "ев", "ие", "ые", "ам", "ям", "ую", "юю",
    "а", "я", "о", "е", "ы", "и", "у", "ю", "й", "ь",
)

# Минимальная длина основы после стемминга — защита от порчи коротких слов.
_MIN_STEM = 3


def _stem(token: str) -> str:
    for suffix in _SUFFIXES:
        if token.endswith(suffix) and len(token) - len(suffix) >= _MIN_STEM:
            return token[: -len(suffix)]
    return token


def normalize(text: str) -> list[str]:
    """Текст -> список токенов-основ для лексического матчинга."""
    expanded: list[str] = []
    for token in _TOKEN_RE.findall(text.lower()):
        expansion = ABBREVIATIONS.get(token)
        if expansion is not None:
            expanded.extend(_TOKEN_RE.findall(expansion))
        else:
            expanded.append(token)

    return [_stem(t) for t in expanded if t not in STOPWORDS]
